/**
 * @file init.command.ts
 * @description Interactive workspace initialization command with guided configuration.
 * Sets up directories, generates secure .env, copies docker-compose sheets,
 * and runs an optional configuration wizard for environment tuning.
 */

import { Command } from "commander";
import * as fs from "node:fs";
import * as path from "node:path";
import * as crypto from "node:crypto";
import chalk from "chalk";
import {
  intro,
  outro,
  confirm,
  select,
  text,
  note,
  logSuccess,
  logWarning,
  logStep,
  createSpinner,
  handleCancel,
  isCancel,
} from "../ui/prompts";
import {
  resolveWorkspaceRoot,
  resolveEnvFile,
  resolveComposeFile,
  writeEnvFile,
  hasTool,
  getSystemInfo,
} from "../ui/platform";

/** Generates a cryptographically secure hex string. */
function secureHex(bytes: number): string {
  return crypto.randomBytes(bytes).toString("hex");
}

/** All workspace directories to create. */
function getWorkspaceDirs(root: string): string[] {
  return [
    path.join(root, "var", "orasaka-uploads"),
    path.join(root, "var", "temp"),
    path.join(root, "var", "logs"),
    path.join(root, "infra"),
    path.join(root, "infra", "local-db"),
  ];
}

